
# Arithmetic Operators:
# +   # Addition
# -   # Subtraction
# *   # Multiplication
# /   # Division
# //  # Floor Division (result is the quotient with fractional part truncated)
# %   # Modulus (remainder of the division)
# **  # Exponentiation
a=10
b=6
print("sum :",a+b)
print("  Subtraction:",a-b)
print("Multiplication :",a*b)
print("Division :",a/b)
print("Floor Division :",a//b)
print("Modulus :",a%b)
print("Exponentiation :",a**b)



# Comparison Operators:
# ==  # Equal to
# !=  # Not equal to
# <   # Less than
# >   # Greater than
# <=  # Less than or equal to
# >=  # Greater than or equal to

n1=5
n2=3
print(n1==n2)
print(n1!=n2)
print(n1<n2)
print(n1>n2)
print(n1<=n2)
print(n1>=n2)

# Logical Operators:
# and   # Logical AND
# or    # Logical OR
# not   # Logical NOT

# Assignment Operators:
# =     # Assign value
# +=    # Add and assign
# -=    # Subtract and assign
# *=    # Multiply and assign
# /=    # Divide and assign
# //=   # Floor divide and assign
# %=    # Modulus and assign
# # **=   # Exponentiate and assign

# Bitwise Operators:
# &   # Bitwise AND
# |   # Bitwise OR
# ^   # Bitwise XOR
# ~   # Bitwise NOT
# <<  # Left shift
# >>  # Right shift

# Membership Operators:
# in     # True if value is found in the sequence
# not in # True if value is not found in the sequence

# Identity Operators:
# is     # True if both variables reference the same object
# is not # True if variables do not reference the same object